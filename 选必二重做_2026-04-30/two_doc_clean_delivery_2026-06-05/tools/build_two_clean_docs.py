def add_contents_page(doc: Document, title: str, rows: list[tuple[str, str]], *, page_break_after: bool = True) -> None:
    p = doc.add_paragraph(title, style="Heading 1")
    p.paragraph_format.space_after = Pt(14)
    for left, right in rows:
        p = doc.add_paragraph()
        p.paragraph_format.space_after = Pt(5)
        r = p.add_run(left)
        set_run(r, size=11.4, bold=True if right else False, color=DEEP_BLUE if right else None)
        if right:
            r = p.add_run(f"    {right}")
            set_run(r, size=10.5, color=TEXT_GRAY)
    if page_break_after:
        doc.add_page_break()


