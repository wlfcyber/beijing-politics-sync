def clean(text: str) -> str:
    text = (text or "").replace("\u3000", " ")
    replacements = {
        "财 产权": "财产权",
        "知识产 权": "知识产权",
        "流通使 用": "流通使用",
        "上述法 律": "上述法律",
        "甲公司公司合法权益": "甲公司合法权益",
        "鲜红龙虾": "鲜红的龙虾",
        "有毒有害等法律、行政法规禁止的内容": "虚假有害信息等法律、行政法规禁止的内容",
        "有毒有害信息": "虚假有害信息",
        "可能违法构筑物确实影响其通行、通风、采光": "该（可能违法的）构筑物确实影响其通行、通风、采光",
        "推进国家治理能力现代化": "推进国家治理体系和治理能力现代化",
        "########## 原题图 ##########": "",
    }
    for old, new in replacements.items():
        text = text.replace(old, new)
    text = text.replace("高三政治二模阅卷总结 2024.5.09\n", "")
    text = text.replace("《高层民用建筑消防安全管理规定）", "《高层民用建筑消防安全管理规定》")
    text = text.replace("问 题指令", "问题指令")
    text = text.replace("关 注 AI", "关注 AI")
    text = text.replace("参考答案 |", "参考答案：")
    text = re.sub(r"\s*\|\s*评分标准说明：?", "", text)
    text = re.sub(r"\s*\|\s*19\.?（2）（8分）", "", text)
    text = re.sub(r"\n?高三(?:年级)?[^\n]{0,20}第\d+页（共\d+页）\n?", "\n", text)
    text = re.sub(r"[ \t]+", " ", text)
    text = re.sub(r"\n{3,}", "\n\n", text)
    return text.strip()


