def add_word_table(doc: Document, caption: str, headers: list[str], rows: list[list[str]], widths: list[float]) -> None:
    if caption:
        p = doc.add_paragraph()
        p.paragraph_format.space_before = Pt(4)
        p.paragraph_format.space_after = Pt(3)
        p.paragraph_format.keep_with_next = True
        r = p.add_run(caption)
        set_run(r, size=10, bold=True, color=DEEP_BLUE)
    table = doc.add_table(rows=1, cols=len(headers))
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    table.autofit = False
    set_table_borders(table, color="C9D7E3")
    keep_row_on_one_page(table.rows[0])
    repeat_table_header(table.rows[0])
    for idx, header in enumerate(headers):
        cell = table.rows[0].cells[idx]
        set_cell_width(cell, widths[idx])
        shade_cell(cell, PALE_BLUE)
        cell_text(cell, header, size=8.8, bold=True, color=DEEP_BLUE, center=True)
    for row in rows:
        table_row = table.add_row()
        keep_row_on_one_page(table_row)
        cells = table_row.cells
        for idx, value in enumerate(row):
            set_cell_width(cells[idx], widths[idx])
            shade_cell(cells[idx], "FFFFFF")
            cell_text(cells[idx], value, size=8.8, center=False)
    doc.add_paragraph().paragraph_format.space_after = Pt(5)


def extract_pipe_table(text: str) -> tuple[str, tuple[list[str], list[list[str]]] | None]:
    raw_lines = [line.strip() for line in clean(text).splitlines()]
    table_lines = [line for line in raw_lines if "|" in line and len([part for part in line.split("|")]) >= 3]
    if len(table_lines) < 2:
        return clean(text), None
    headers = [clean(part) for part in table_lines[0].split("|")]
    rows = [[clean(part) for part in line.split("|")] for line in table_lines[1:]]
    keep_lines = [line for line in raw_lines if line not in table_lines]
    width = len(headers)
    rows = [row[:width] + [""] * max(0, width - len(row)) for row in rows]
    return "\n".join(keep_lines).strip(), (headers, rows)


