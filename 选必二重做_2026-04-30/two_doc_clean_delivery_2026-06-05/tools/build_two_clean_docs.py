def extract_image_assets() -> dict[str, dict[str, list[Path]]]:
    IMAGE_ASSET_DIR.mkdir(parents=True, exist_ok=True)
    assets: dict[str, dict[str, list[Path]]] = defaultdict(lambda: {"question": [], "rubric": []})
    packet = find_image_packet_docx()
    if packet:
        rel_ns = "{http://schemas.openxmlformats.org/package/2006/relationships}"
        w_ns = "{http://schemas.openxmlformats.org/wordprocessingml/2006/main}"
        a_ns = "{http://schemas.openxmlformats.org/drawingml/2006/main}"
        r_ns = "{http://schemas.openxmlformats.org/officeDocument/2006/relationships}"
        with ZipFile(packet, "r") as zf:
            rel_root = ET.fromstring(zf.read("word/_rels/document.xml.rels"))
            rels = {rel.attrib["Id"]: rel.attrib["Target"] for rel in rel_root.findall(f"{rel_ns}Relationship")}
            doc_root = ET.fromstring(zf.read("word/document.xml"))
            current_title = ""
            current_kind = ""
            for para in doc_root.iter(f"{w_ns}p"):
                text = "".join(node.text or "" for node in para.iter(f"{w_ns}t")).strip()
                if re.match(r"^\d{4} · ", text):
                    current_title = text
                    current_kind = ""
                elif text == "试题图片":
                    current_kind = "question"
                elif text == "细则图片":
                    current_kind = "rubric"
                for blip in para.iter(f"{a_ns}blip"):
                    rid = blip.attrib.get(f"{r_ns}embed")
                    if not (rid and current_title and current_kind and rid in rels):
                        continue
                    target = rels[rid]
                    media_name = "word/" + target if not target.startswith("word/") else target
                    suffix = Path(target).suffix or ".jpg"
                    key = canonical_title(current_title)
                    index = len(assets[key][current_kind]) + 1
                    dest = IMAGE_ASSET_DIR / f"{safe_filename(current_title)}_{current_kind}_{index}{suffix}"
                    if not dest.exists():
                        dest.write_bytes(zf.read(media_name))
                    assets[key][current_kind].append(dest)
    if IMAGE_FALLBACK_E074.exists():
        try:
            from PIL import Image

            dest = IMAGE_ASSET_DIR / "2024_顺义思政_二模_第17题_question_1.png"
            if not dest.exists():
                with Image.open(IMAGE_FALLBACK_E074) as img:
                    img.save(dest)
            assets[canonical_title("2024 · 顺义思政 · 二模 · 第17题")]["question"].append(dest)
        except Exception:
            pass
    return dict(assets)


