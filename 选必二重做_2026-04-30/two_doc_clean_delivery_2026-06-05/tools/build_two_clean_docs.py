def student_paragraphs(text: str) -> list[str]:
    text = clean(text)
    if not text:
        return ["（空）"]
    text = re.sub(r"\s*\|\s*", "；", text)
    text = re.sub(
        r"(?<!\n)(【[^】]{1,20}】|材料[一二三四五六七八九十0-9]|案例\s*[一二三四五六七八九十0-9]|案件\s*[一二三四五六七八九十0-9]|基本案情|裁判结果|裁判理由及意义|裁判理由|评分细则|评分标准说明|等级水平|参考答案|解析要点)",
        r"\n\1",
        text,
    )
    paragraphs = []
    for block in re.split(r"\n\s*\n", text):
        lines = [line.strip() for line in block.splitlines() if line.strip()]
        if not lines:
            continue
        units = []
        current = ""
        for line in lines:
            is_heading = bool(re.match(DISPLAY_HEADING_PATTERN, line))
            if is_heading:
                if current:
                    units.append(current)
                current = line
            else:
                current = f"{current}{line}" if current else line
        if current:
            units.append(current)
        for unit in units:
            if len(unit) <= 240:
                paragraphs.append(unit)
                continue
            pieces = [piece for piece in re.split(r"(?<=[。；;])", unit) if clean(piece)]
            current = ""
            for piece in pieces:
                piece = clean(piece)
                if current and len(current) + len(piece) > 240:
                    paragraphs.append(current)
                    current = piece
                else:
                    current += piece
            if current:
                paragraphs.append(current)
    return paragraphs or ["（空）"]


