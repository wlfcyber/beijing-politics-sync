def is_display_heading(line: str) -> bool:
    return bool(re.match(DISPLAY_HEADING_PATTERN, line))


def split_display_heading(line: str) -> tuple[str, str]:
    match = re.match(
        r"^(【[^】]+】|材料[一二三四五六七八九十0-9]|案例\s*[一二三四五六七八九十0-9]|案件\s*[一二三四五六七八九十0-9]|基本案情|裁判结果|裁判理由及意义|裁判理由|评分细则|评分标准说明|等级水平|参考答案|解析要点)(.*)$",
        line,
    )
    if not match:
        return "", line
    return match.group(1), match.group(2).strip()


def add_label(doc: Document, label: str, text: str, *, fill: str = LIGHT_GRAY) -> None:
    table = doc.add_table(rows=1, cols=2)
    table.alignment = WD_TABLE_ALIGNMENT.LEFT
    table.autofit = False
    left, right = table.rows[0].cells
    set_cell_width(left, 1.15)
    set_cell_width(right, 5.6)
    shade_cell(left, fill)
    shade_cell(right, "FFFFFF")
    for cell in table.rows[0].cells:
        cell.vertical_alignment = WD_CELL_VERTICAL_ALIGNMENT.TOP
        set_cell_margins(cell)
    p = left.paragraphs[0]
    p.paragraph_format.space_after = Pt(0)
    r = p.add_run(label)
    set_run(r, size=9.6, bold=True, color=BLUE)
    p = right.paragraphs[0]
    p.paragraph_format.space_after = Pt(0)
    for idx, line in enumerate((clean(text) or "（空）").splitlines()):
        if idx:
            p.add_run().add_break()
        r = p.add_run(line)
        set_run(r, size=9.6)
    doc.add_paragraph().paragraph_format.space_after = Pt(1)


def pipe_widths(headers: list[str]) -> list[float]:
    if len(headers) == 4:
        return [0.9, 1.15, 2.55, 1.75]
    if len(headers) == 3:
        return [3.15, 1.35, 2.05]
    if len(headers) == 2:
        return [1.25, 5.0]
    base = 6.35 / max(1, len(headers))
    return [base] * len(headers)


def special_intro_text(row: dict, field: str, text: str) -> str:
    eid = row.get("entry_id")
    text = clean(text)
    if eid == "E029" and field == "material":
        return text.split("表格转写如下：", 1)[0].strip()
    if eid == "E033" and field == "prompt":
        return "参考示例，完成下表。"
    if eid == "E050" and field == "prompt":
        return "阅读材料二，运用《法律与生活》知识，完成下表。"
    if eid == "E059" and field == "material":
        return "阅读材料，完成下表。"
    return text


def add_text_block(doc: Document, label: str, text: str, *, row: dict | None = None, field: str = "") -> None:
    row = row or {}
    text = special_intro_text(row, field, text)
    text, pipe_table = extract_pipe_table(text)
    specs = special_table_specs(row, field)

    label_color = {
        "材料": MID_BLUE,
        "设问": GOLD,
        "细则": TEXT_GRAY,
    }.get(label, DEEP_BLUE)
    fill = {
        "材料": PALE_BLUE,
        "设问": PALE_GOLD,
        "细则": "F7F7F7",
    }.get(label)
    border = {
        "材料": MID_BLUE,
        "设问": GOLD,
        "细则": "A6ADB5",
    }.get(label)

    if text:
        first = True
        for line in student_paragraphs(text):
            p = doc.add_paragraph()
            p.paragraph_format.left_indent = Inches(0.08)
            p.paragraph_format.first_line_indent = Inches(0)
            p.paragraph_format.space_before = Pt(2 if first else 0)
            p.paragraph_format.space_after = Pt(3)
            p.paragraph_format.line_spacing = 1.28
            if fill and first:
                set_paragraph_shading(p, fill)
                set_paragraph_border(p, left=border, size="14")
            elif label == "设问":
                set_paragraph_shading(p, fill or PALE_GOLD)
                set_paragraph_border(p, left=border or GOLD, size="14")
            if first:
                r = p.add_run(f"【{label}】")
                set_run(r, size=10.8 if label != "细则" else 10.2, bold=True, color=label_color)
                r = p.add_run(" ")
                set_run(r, size=10.4)
            if is_display_heading(line):
                prefix, rest = split_display_heading(line)
                if prefix:
                    r = p.add_run(prefix)
                    set_run(r, size=10.3 if label != "细则" else 9.8, bold=True, color=label_color)
                    if rest:
                        r = p.add_run(" " + rest)
                        set_run(r, size=10.2 if label != "细则" else 9.8)
                else:
                    r = p.add_run(line)
                    set_run(r, size=10.3 if label != "细则" else 9.8, bold=True, color=label_color)
            else:
                r = p.add_run(line)
                set_run(r, size=10.2 if label != "细则" else 9.6)
            first = False

    if pipe_table:
        headers, rows = pipe_table
        add_word_table(doc, f"{label}表格重建", headers, rows, pipe_widths(headers))
    for caption, headers, rows, widths in specs:
        add_word_table(doc, caption, headers, rows, widths)


def add_points(doc: Document, label: str, points: list[str], *, core: bool = False) -> None:
    if core:
        for idx, point in enumerate(points):
            p = doc.add_paragraph()
            p.paragraph_format.space_before = Pt(4 if idx == 0 else 0)
            p.paragraph_format.space_after = Pt(4)
            p.paragraph_format.left_indent = Inches(0.08)
            set_paragraph_shading(p, PALE_RED)
            set_paragraph_border(p, left=ANSWER_RED, size="14")
            if idx == 0:
                r = p.add_run(f"【{label}】")
                set_run(r, size=11.0, bold=True, color=ANSWER_RED)
                r = p.add_run(" ")
                set_run(r, size=10.6)
            r = p.add_run(point)
            set_run(r, size=10.6, bold=False, color=ANSWER_RED if point.startswith("核心") else None)
        return
    for idx, point in enumerate(points):
        p = doc.add_paragraph()
        p.paragraph_format.left_indent = Inches(0.08)
        p.paragraph_format.space_before = Pt(3 if idx == 0 else 0)
        p.paragraph_format.space_after = Pt(4)
        p.paragraph_format.line_spacing = 1.28
        set_paragraph_shading(p, PALE_GREEN)
        set_paragraph_border(p, left=TEAL, size="14")
        if idx == 0:
            r = p.add_run(f"【{label}】")
            set_run(r, size=10.8, bold=True, color=TEAL)
            r = p.add_run(" ")
            set_run(r, size=10.4)
        r = p.add_run(point)
        set_run(r, size=10.2)


def add_images(doc: Document, label: str, paths: list[Path]) -> None:
    if not paths:
        return
    doc.add_paragraph(label, style="Heading 3")
    for path in paths:
        if not Path(path).exists():
            continue
        try:
            doc.add_picture(str(path), width=Inches(6.25))
            doc.paragraphs[-1].paragraph_format.space_after = Pt(5)
        except Exception:
            p = doc.add_paragraph()
            r = p.add_run(f"{label}图片暂无法嵌入")
            set_run(r, size=9.2)


