def qa_scan(path: Path) -> dict:
    text = path.read_text(encoding="utf-8") if path.suffix == ".md" else ""
    bad_terms = [
        "三十秒", "速记", "工程痕迹", "工程说明", "source_id", "SRC_",
        "entry_E", "制作记录", "命题人路径", "判题四步", "高频给分件",
        "易混边界", "教师复核", "/Users/wanglifei", "当前源包", "待核",
        "TODO", "BLOCKED",
    ]
    counts = {term: text.count(term) for term in bad_terms}
    counts["E编号"] = len(re.findall(r"\bE\d{3}\b", text))
    return counts


