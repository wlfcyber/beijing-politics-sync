def split_long_point(text: str, max_len: int = 150) -> list[str]:
    if len(text) <= max_len:
        return [text]
    pieces = [piece for piece in re.split(r"(?<=[，。；;])", text) if clean(piece)]
    if len(pieces) <= 1:
        return [text]
    chunks = []
    current = ""
    for piece in pieces:
        piece = clean(piece)
        if current and len(current) + len(piece) > max_len:
            chunks.append(current)
            current = piece
        else:
            current += piece
    if current:
        chunks.append(current)
    return chunks


def clean_answer_point(point: str) -> str:
    point = clean(point)
    previous = None
    while previous != point:
        previous = point
        point = re.sub(r"^\s*\d+\s*[.．、]?\s*（[^）]*分）\s*", "", point)
        point = re.sub(r"^\s*（\d+）\s*（[^）]*分）\s*", "", point)
    point = re.sub(r"^参考答案[:：]\s*", "", point)
    return point.strip()


def answer_points(row: dict, limit: int = 7) -> list[str]:
    if row.get("answer_points"):
        return [clean_answer_point(point) for point in row["answer_points"][:limit] if clean_answer_point(point)]
    text = clean(row.get("rubric_clean", ""))
    if not text:
        return ["细则文本暂缺。"]
    raw_parts = []
    for line in student_paragraphs(text):
        line = clean(line)
        if not line:
            continue
        if any(noise in line for noise in [
            "学生表现", "教学启示", "教师教学", "改进措施", "阅卷总结",
            "任务类型", "采点赋分", "评分细则", "评分标准", "准确回答",
            "准确分析", "答非所问", "没有应答", "不能得满分",
        ]):
            continue
        chunks = re.split(r"(?<=[。；;])", line)
        raw_parts.extend(chunks if len(chunks) > 1 else [line])
    points = []
    for part in raw_parts:
        part = clean_answer_point(part)
        if len(part) < 6:
            continue
        for chunk in split_long_point(part):
            chunk = clean_answer_point(chunk)
            if not chunk:
                continue
            points.append(chunk)
            if len(points) >= limit:
                break
        if len(points) >= limit:
            break
    return points or [text]


def md_entry(row: dict, groups: dict[str, list[dict]], *, with_axis: bool = False) -> list[str]:
    title = safe_title(row["title"])
    lines = [f"### {title}", ""]
    lines.append(f"- 题目来源：{title}")
    if with_axis:
        axis_text = row.get("axis_note") or f"{A_NAMES[row['a_axis']]}｜{B_NAMES[row['b_axis']]}"
        lines.append(f"- 框架归位：{axis_text}")
    lines.append(f"- 同题组：{same_group_text(row, groups)}")
    lines.extend(["", "#### 材料", clean(row.get("material", "")) or "（材料文本暂缺。）"])
    question_images = student_question_images(row)
    if question_images:
        lines.extend(["", "#### 原题图"])
        for idx, path in enumerate(question_images, start=1):
            lines.append(f"![原题图{idx}]({md_asset_ref(path)})")
    lines.extend([
        "",
        "#### 设问",
        clean(row.get("prompt", "")) or "（设问文本暂缺。）",
        "",
        "#### 细则",
        clean(row.get("rubric_clean", "")) or "（细则文本暂缺。）",
    ])
    rubric_images = student_rubric_images(row)
    if rubric_images:
        lines.extend(["", "#### 细则图"])
        for idx, path in enumerate(rubric_images, start=1):
            lines.append(f"![细则图{idx}]({md_asset_ref(path)})")
    lines.extend(["", "#### 答案落点"])
    for point in answer_points(row):
        lines.append(f"- {point}")
    lines.append("")
    return lines


