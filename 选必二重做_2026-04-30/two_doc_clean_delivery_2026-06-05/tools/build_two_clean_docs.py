def copy_to_desktop() -> None:
    desktop = Path.home() / "Desktop"
    for path in [COMPILATION_DOCX, BAODIAN_DOCX]:
        if path.exists():
            shutil.copy2(path, desktop / path.name)


def main() -> None:
    OUT_DIR.mkdir(parents=True, exist_ok=True)
