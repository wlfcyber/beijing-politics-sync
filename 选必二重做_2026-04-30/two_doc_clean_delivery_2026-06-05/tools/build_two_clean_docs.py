    rows = [
        item
        for item in groups.get(row["same_group_key"], [])
        if item.get("entry_id") != row.get("entry_id")
    ]
    if not rows:
        return "本题组仅本分问。"
    return "；".join(safe_title(item["title"]) for item in rows)


