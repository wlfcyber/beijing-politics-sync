def split_points(text: str, limit: int = 9) -> list[str]:
    text = clean(text)
    if not text:
        return []
    chunks = re.split(r"(?<=[。；;])\s*|(?=\s*[①②③④⑤⑥⑦⑧])|(?=【[^】]{2,18}】)", text)
    points: list[str] = []
    for chunk in chunks:
        item = clean(chunk.strip(" ；;。"))
        if not item or len(item) < 6:
            continue
        if item not in points:
            points.append(item + ("。" if not item.endswith(("。", "；", "）", ")")) else ""))
        if len(points) >= limit:
            break
    return points or [clean(text, 360)]


def slice_by_rule(label: str, text: str) -> str:
    rule = RUBRIC_SLICE_RULES.get(label)
    if not rule:
        return text
    sliced = text
    start = rule.get("start")
    end = rule.get("end")
    if start and start in sliced:
        sliced = sliced[sliced.index(start) :]
    if end and end in sliced:
        sliced = sliced[: sliced.index(end)]
    return sliced.strip()


