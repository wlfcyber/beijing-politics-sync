def numbered_statements(row: dict) -> str:
    text = clean(row.get("prompt"))
    if "①" not in text:
        return ""
    statements = []
    for marker, next_marker in zip("①②③④", "②③④A"):
        start = text.find(marker)
        end = text.find(next_marker, start + 1) if start != -1 else -1
        if start == -1 or end == -1:
            continue
        statement = clean(text[start:end], 140)
        if statement:
            statements.append(statement)
    return "；".join(statements)


