def node_sources(cell: str, core_ids: set[str]) -> tuple[list[str], list[str]]:
    core: list[str] = []
    other: list[str] = []
    for raw in re.split(r"[;；]", cell or ""):
        item = raw.strip()
        if not item:
            continue
        base = item.split("(")[0].replace("-sub2", "").replace("-sub1", "").strip()
        if base in core_ids or base.split("__sub")[0] in core_ids:
            core.append(item)
        else:
            other.append(item)
    return core, other


