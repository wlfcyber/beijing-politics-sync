def read_csv(name: str) -> list[dict[str, str]]:
    with (BASE / name).open(encoding="utf-8-sig", newline="") as f:
        return list(csv.DictReader(f))


def write_csv(name: str, rows: list[dict[str, str]], fieldnames: list[str]) -> None:
    with (BASE / name).open("w", encoding="utf-8-sig", newline="") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames)
        w.writeheader()
        w.writerows(rows)


def section(text: str, title: str) -> str:
    pattern = rf"## {re.escape(title)}.*?\n```\n(.*?)\n```"
    m = re.search(pattern, text, flags=re.S)
    return m.group(1).strip() if m else ""


def clean(s: str, limit: int | None = None) -> str:
    s = re.sub(r"===== *PAGE *\d+ *=====", " ", s, flags=re.I)
    s = re.sub(r"===== *SLIDE *\d+ *=====", " ", s, flags=re.I)
    s = re.sub(r"第\s*\d+\s*页[/／]共\s*\d+\s*页", " ", s)
    s = re.sub(r"第\s*\d+\s*页\s*[（(]共\s*\d+\s*页[）)]", " ", s)
    s = re.sub(r"[\ue000-\uf8ff]", " ", s)
    s = re.sub(r"\s+", " ", s).strip()
    if limit and len(s) > limit:
        s = s[:limit].rstrip(" ，。；、") + "……"
    return s


