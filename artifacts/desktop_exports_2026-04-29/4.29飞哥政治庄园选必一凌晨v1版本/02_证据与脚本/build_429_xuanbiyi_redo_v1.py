def set_cell_shading(cell, fill):
    tc_pr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:fill"), fill)
    tc_pr.append(shd)


def set_cell_text(cell, text, bold=False):
    cell.text = ""
    p = cell.paragraphs[0]
    r = p.add_run(text)
    r.bold = bold
    r.font.name = "宋体"
    r._element.rPr.rFonts.set(qn("w:eastAsia"), "宋体")
    r.font.size = Pt(10.5)


def add_case_table(doc, case):
    table = doc.add_table(rows=5, cols=2)
    table.style = "Table Grid"
    labels = ["完整设问", "细则位置", "来源", "材料触发", "答案句"]
    for i, label in enumerate(labels):
        table.cell(i, 0).width = Cm(3)
        table.cell(i, 1).width = Cm(13.2)
        set_cell_shading(table.cell(i, 0), "EAF2F8")
        set_cell_text(table.cell(i, 0), label, True)
        set_cell_text(table.cell(i, 1), case[label], False)
    doc.add_paragraph()


