def add_markdown_to_doc(doc: Document, markdown: str) -> None:
    in_code = False
    for raw_line in markdown.splitlines():
        line = raw_line.rstrip()
        if not line:
            continue
        if line.startswith("```"):
            in_code = not in_code
            continue
        if in_code:
            p = doc.add_paragraph(style="Normal")
            run = p.add_run(line)
            run.font.name = "Consolas"
            run.font.size = Pt(9.5)
            set_east_asian_font(run)
            continue
        if line.startswith("# "):
            continue
        if line.startswith("## "):
            p = doc.add_paragraph(style="Heading 1")
            run = p.add_run(clean_inline(line[3:]))
        elif line.startswith("### "):
            p = doc.add_paragraph(style="Heading 2")
            run = p.add_run(clean_inline(line[4:]))
        elif line.startswith("- "):
            p = doc.add_paragraph(style="List Bullet")
            run = p.add_run(clean_inline(line[2:]))
        elif re.match(r"^\d+\.\s+", line):
            p = doc.add_paragraph(style="List Number")
            run = p.add_run(clean_inline(re.sub(r"^\d+\.\s+", "", line)))
        else:
            p = doc.add_paragraph(style="Normal")
            run = p.add_run(clean_inline(line))
        set_east_asian_font(run)


def build_docx(markdown: str, docx_path: Path, title: str, subtitle: str) -> None:
    doc = Document()
    configure_document(doc, title, subtitle)
    add_markdown_to_doc(doc, markdown)
    for table in doc.tables:
        set_table_width(table)
    doc.core_properties.title = title
    doc.core_properties.subject = subtitle
    doc.core_properties.comments = "Preset: compact_reference_guide; CJK font override: Microsoft YaHei."
    doc.save(docx_path)


