def write_csv() -> Path:
    path = OUT_DIR / "pdf_original_framework.csv"
    with path.open("w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=list(ROWS[0].keys()))
        writer.writeheader()
        writer.writerows(ROWS)
    return path


def write_md() -> Path:
    path = OUT_DIR / "pdf_original_framework.md"
    order = ["时代背景", "理论", "经济全球化", "政治多极化", "中国", "联合国"]
    lines = [
        "# 选必一 PDF 原框架抽取",
        "",
        "来源：`/Users/wanglifei/Desktop/先前框架/选必一 原文件 拷贝 (8)(2).pdf`",
        "",
        "规则：本文件只使用 PDF 前 7 页中可读的六要素框架；8-18 页未识别为新的原框架页。",
        "",
    ]
    for element in order:
        lines.append(f"## {element}")
        frames = []
        for r in ROWS:
            if r["六要素"] == element and r["小框架"] not in frames:
                frames.append(r["小框架"])
        for frame in frames:
            terms = [r for r in ROWS if r["六要素"] == element and r["小框架"] == frame]
            pages = sorted({str(r["PDF页码"]) for r in terms}, key=lambda x: int(x) if x.isdigit() else 999)
            lines.append(f"### {frame}")
            lines.append(f"- PDF页码：{', '.join(pages)}")
            for r in terms:
                confirm = "（NEED_USER_CONFIRMATION）" if r["是否需要用户确认"] == "是" else ""
                lines.append(f"- {r['规范化表述']}{confirm}")
            lines.append("")
    path.write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")
    return path


