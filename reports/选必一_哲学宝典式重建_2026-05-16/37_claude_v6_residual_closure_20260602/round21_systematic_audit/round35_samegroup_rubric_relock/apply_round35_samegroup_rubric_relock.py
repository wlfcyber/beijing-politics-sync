def set_text(p: etree._Element, text: str) -> None:
    runs = p.xpath(".//w:t", namespaces=NS)
    if not runs:
        return
    runs[0].text = text
    for run in runs[1:]:
        run.text = ""


def ensure(parent: etree._Element, child_tag: str, *, first: bool = False) -> etree._Element:
    found = parent.find(qn(child_tag))
    if found is not None:
        return found
    child = etree.Element(qn(child_tag))
    if first:
        parent.insert(0, child)
    else:
        parent.append(child)
    return child


def set_run_deep_red(run: etree._Element) -> None:
    rpr = ensure(run, "w:rPr", first=True)
    bold = ensure(rpr, "w:b")
    bold.set(qn("w:val"), "1")
    color = ensure(rpr, "w:color")
    color.set(qn("w:val"), "C00000")


def emphasize_core_headings(body: etree._Element) -> int:
    changed = 0
    for p in body.xpath(".//w:p", namespaces=NS):
        text = paragraph_text(p)
        if not text.startswith("核心答题点："):
            continue
        ppr = ensure(p, "w:pPr", first=True)
        p_rpr = ensure(ppr, "w:rPr")
        p_bold = ensure(p_rpr, "w:b")
        p_bold.set(qn("w:val"), "1")
        p_color = ensure(p_rpr, "w:color")
        p_color.set(qn("w:val"), "C00000")
        for run in p.xpath(".//w:r", namespaces=NS):
            set_run_deep_red(run)
        changed += 1
    return changed


def is_entry_title(text: str) -> bool:
    return bool(re.match(r"^\d+\.\s*20\d{2}", text))


def clean_key_title(title: str) -> str | None:
    compact = title.replace(" ", "").replace("细则复练", "")
    m = re.search(r"(20\d{2}.+?Q\d+(?:\(\d+\))?)", compact)
    return m.group(1) if m else None


def is_heading(text: str, p: etree._Element) -> bool:
    style = p.xpath("./w:pPr/w:pStyle/@w:val", namespaces=NS)
    return text.startswith("核心答题点：") or bool(style and style[0] in {"1", "2", "3", "4", "5"})


def replace_samegroups(body: etree._Element) -> dict:
    replaced = 0
    details: list[dict] = []
    current_key: str | None = None
    i = 0
    while i < len(body):
        p = body[i]
        txt = paragraph_text(p) if p.tag == qn("w:p") else ""
        if is_entry_title(txt):
            current_key = clean_key_title(txt)
        elif is_heading(txt, p):
            current_key = None

        if current_key in SAME_GROUP_OVERRIDES and txt.startswith("【同题组】"):
            set_text(p, "【同题组】  （按原题评分层次）")
            j = i + 1
            removed = 0
            while j < len(body):
                t = paragraph_text(body[j])
                if not t.startswith("· "):
                    break
                body.remove(body[j])
                removed += 1
            lines = SAME_GROUP_OVERRIDES[current_key]
            for offset, line in enumerate(lines, start=1):
                body.insert(i + offset, make_bullet(line))
            replaced += 1
            details.append({"key": current_key, "removed_lines": removed, "inserted_lines": len(lines)})
            i += len(lines)
        i += 1
    return {"replaced": replaced, "details": details}


def main() -> None:
    with tempfile.TemporaryDirectory() as td:
        tmp = Path(td)
        with ZipFile(INPUT) as zin:
            zin.extractall(tmp)
        doc_xml = tmp / "word" / "document.xml"
        root = etree.fromstring(doc_xml.read_bytes())
        body = root.find(qn("w:body"))
        assert body is not None
        report = replace_samegroups(body)
        report["core_headings_deep_red"] = emphasize_core_headings(body)
        doc_xml.write_bytes(etree.tostring(root, xml_declaration=True, encoding="UTF-8", standalone=True))

        if OUTPUT.exists():
            OUTPUT.unlink()
        with ZipFile(OUTPUT, "w", ZIP_DEFLATED) as zout:
            for path in sorted(tmp.rglob("*")):
                if path.is_file():
                    zout.write(path, path.relative_to(tmp).as_posix())
    FINAL.write_bytes(OUTPUT.read_bytes())
    print(report)
    print(f"wrote={OUTPUT}")
    print(f"final={FINAL}")


