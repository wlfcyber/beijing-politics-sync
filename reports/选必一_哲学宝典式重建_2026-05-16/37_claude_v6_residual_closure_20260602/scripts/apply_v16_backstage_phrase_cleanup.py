def set_paragraph_text(paragraph, text: str) -> None:
    if paragraph.runs:
        paragraph.runs[0].text = text
        for run in paragraph.runs[1:]:
            run.text = ""
    else:
        paragraph.add_run(text)


def iter_paragraphs(doc: Document):
    yield from doc.paragraphs
    for table in doc.tables:
        for row in table.rows:
            for cell in row.cells:
                yield from cell.paragraphs


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--input-docx", type=Path, required=True)
    parser.add_argument("--output-docx", type=Path, required=True)
    parser.add_argument("--log-json", type=Path, required=True)
    args = parser.parse_args()

    shutil.copy2(args.input_docx, args.output_docx)
    doc = Document(args.output_docx)

    log: list[dict] = []
    for para in iter_paragraphs(doc):
        original = para.text
        updated = REPLACEMENTS.get(original)
        if updated is None:
            continue
        set_paragraph_text(para, updated)
        log.append({"old": original, "new": updated})

    missing = [old for old in REPLACEMENTS if old not in {item["old"] for item in log}]
    doc.save(args.output_docx)
    args.log_json.write_text(
        json.dumps(
            {
                "input": str(args.input_docx),
                "output": str(args.output_docx),
                "changes": len(log),
                "missing": missing,
                "items": log,
            },
            ensure_ascii=False,
            indent=2,
        ),
        encoding="utf-8",
    )
    print(json.dumps({"changes": len(log), "missing": len(missing), "output": str(args.output_docx)}, ensure_ascii=False))
    if missing:
        raise SystemExit(1)


if __name__ == "__main__":
    main()
