def set_paragraph_text(paragraph, text: str) -> None:
    if paragraph.runs:
        paragraph.runs[0].text = text
        for run in paragraph.runs[1:]:
            run.text = ""
    else:
        paragraph.add_run(text)


def remove_paragraph(paragraph) -> None:
    element = paragraph._element
    parent = element.getparent()
    if parent is not None:
        parent.remove(element)


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--input-docx", type=Path, required=True)
    parser.add_argument("--output-docx", type=Path, required=True)
    parser.add_argument("--log-json", type=Path, required=True)
    args = parser.parse_args()

    shutil.copy2(args.input_docx, args.output_docx)
    doc = Document(args.output_docx)
    log: list[dict] = []

    for paragraph in list(doc.paragraphs):
        text = paragraph.text.strip()
        if text.startswith("【同题内分层") and text.endswith("】"):
            log.append({"kind": "delete", "old": text})
            remove_paragraph(paragraph)
            continue
        if text in REPLACEMENTS:
            new_text = REPLACEMENTS[text]
            set_paragraph_text(paragraph, new_text)
            log.append({"kind": "replace", "old": text, "new": new_text})

    doc.save(args.output_docx)
    args.log_json.write_text(json.dumps(log, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"replacements={sum(1 for item in log if item['kind'] == 'replace')}")
    print(f"deletions={sum(1 for item in log if item['kind'] == 'delete')}")


if __name__ == "__main__":
    main()
