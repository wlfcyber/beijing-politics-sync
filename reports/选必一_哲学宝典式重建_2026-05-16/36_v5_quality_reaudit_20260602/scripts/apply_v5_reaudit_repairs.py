def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("input_docx", type=Path)
    parser.add_argument("output_docx", type=Path)
    args = parser.parse_args()

    shutil.copy2(args.input_docx, args.output_docx)
    doc = Document(args.output_docx)
    entries = parse_entries(doc)
    applied: list[dict] = []
    errors: list[str] = []

    for rule in REPAIRS:
        hits = [entry for entry in entries if matches(entry, rule)]
        if not hits:
            errors.append(f"MISS title={rule.get('title')} h1={rule.get('h1','')} h3={rule.get('h3','')}")
            continue
        if len(hits) > 1 and rule.get("h1") is None and rule.get("h2") is None:
            errors.append(f"AMBIG title={rule.get('title')} h3={rule.get('h3','')} hits={len(hits)}")
            continue
        for entry in hits:
            changed = []
            for label, value in rule["fields"].items():
                para = entry["field_paras"].get(label)
                if para is None:
                    errors.append(f"NO_FIELD {label} title={entry['title']} h3={entry['h3']}")
                    continue
                set_text(para, f"【{label}】 {value}")
                changed.append(label)
            applied.append({"idx": entry["idx"], "title": entry["title"], "h1": entry["h1"], "h3": entry["h3"], "fields": changed})

    if errors:
        print("ERRORS")
        for err in errors:
            print(err)
        raise SystemExit(1)

    doc.save(args.output_docx)
    print(f"applied={len(applied)}")
    for item in applied:
        print(f"{item['idx']} | {item['title']} | {item['h1']} | {item['h3']} | {','.join(item['fields'])}")


if __name__ == "__main__":
    main()
