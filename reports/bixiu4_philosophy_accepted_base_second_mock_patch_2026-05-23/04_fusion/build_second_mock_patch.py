def basic_text_gate() -> None:
    doc = Document(str(OUT_DOCX))
    text = "\n".join(p.text for p in doc.paragraphs)
    forbidden = ["C:\\", "OCR", "PASS", "Governor", "ClaudeCode", "source_texts", "评标", "细则："]
    hits = [token for token in forbidden if token in text]
    if hits:
        raise RuntimeError(f"Forbidden student-facing tokens found: {hits}")
    missing = [item["source"] for item in ENTRIES if item["source"] not in text]
    if missing:
        raise RuntimeError(f"Inserted titles missing from DOCX text: {missing[:5]}")


