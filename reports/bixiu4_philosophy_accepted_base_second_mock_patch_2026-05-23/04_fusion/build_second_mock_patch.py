def insert_paragraph_after(paragraph: Paragraph, text: str = "", style: str | None = None) -> Paragraph:
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_para = Paragraph(new_p, paragraph._parent)
    if style:
        new_para.style = style
    if text:
        new_para.add_run(text)
    return new_para


def paragraph_style_name(paragraph: Paragraph) -> str:
    try:
        return paragraph.style.name or ""
    except Exception:
        return ""


def find_section_bounds(doc: Document, section: str) -> tuple[int, int]:
    paragraphs = doc.paragraphs
    start = None
    for i, paragraph in enumerate(paragraphs):
        if paragraph_style_name(paragraph) == "Heading 2" and paragraph.text.strip() == section:
            start = i
            break
    if start is None:
        raise ValueError(f"Section not found: {section}")
    end = len(paragraphs)
    for j in range(start + 1, len(paragraphs)):
        style = paragraph_style_name(paragraphs[j])
        if style in {"Heading 1", "Heading 2"}:
            end = j
            break
    return start, end


def next_entry_number(doc: Document, start: int, end: int) -> int:
    numbers = []
    for paragraph in doc.paragraphs[start + 1 : end]:
        if paragraph_style_name(paragraph) == "Heading 3":
            match = re.match(r"\s*(\d+)[.．]", paragraph.text.strip())
            if match:
                numbers.append(int(match.group(1)))
    return (max(numbers) + 1) if numbers else 1


def append_entries_to_docx() -> dict:
    doc = Document(str(BASE_DOCX))
    grouped = defaultdict(list)
    for item in ENTRIES:
        grouped[item["section"]].append(item)

    insertion_summary = {}
    for section, items in grouped.items():
        start, end = find_section_bounds(doc, section)
        number = next_entry_number(doc, start, end)
        anchor = doc.paragraphs[end - 1]
        inserted = []
        for item in items:
            title = f"{number}. {item['source']}（{item['kind']}）"
            anchor = insert_paragraph_after(anchor, title, "Heading 3")
            anchor = insert_paragraph_after(anchor, f"【材料触发点】{item['trigger']}", "Normal")
            anchor = insert_paragraph_after(anchor, f"【设问】{item['question']}", "Normal")
            anchor = insert_paragraph_after(anchor, f"【为什么能想到】{item['why']}", "Normal")
            anchor = insert_paragraph_after(anchor, f"【答案落点】{item['answer']}", "Normal")
            anchor = insert_paragraph_after(anchor, "", "Normal")
            inserted.append(title)
            number += 1
        insertion_summary[section] = inserted

    doc.save(str(OUT_DOCX))
    shutil.copy2(OUT_DOCX, DESKTOP_COPY)
    return insertion_summary


