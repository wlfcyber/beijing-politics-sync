def insert_block(section: str, block: str) -> str:
    if "【完整选项】" in section:
        return section
    matches = list(re.finditer(r"<!--.*?-->\s*", section, re.S))
    if matches:
        pos = matches[-1].end()
        return section[:pos].rstrip() + "\n\n" + block.strip() + "\n\n" + section[pos:].lstrip()
    return block.strip() + "\n\n" + section.lstrip()


def main() -> None:
    text = BODY.read_text(encoding="utf-8")
    matches = list(re.finditer(r"^###\s+(.+?)\s*$", text, re.M))
    parts: list[str] = []
    patched: list[str] = []
    last = 0
    for i, match in enumerate(matches):
        title = match.group(1).strip()
        start = match.end()
        end = matches[i + 1].start() if i + 1 < len(matches) else len(text)
        parts.append(text[last:start])
        section = text[start:end]
        qid_match = re.search(r"question_id:\s*([^;]+);", section)
        qid = qid_match.group(1).strip() if qid_match else ""
        if qid in OPTION_BLOCKS:
            before = section
            section = insert_block(section, OPTION_BLOCKS[qid])
            if section != before:
                patched.append(qid)
        parts.append(section)
        last = end
    parts.append(text[last:])
    BODY.write_text("".join(parts), encoding="utf-8")

    PATCH_LOG.write_text(
        "\n".join(
            [
                "# Phase12 Choice Full Option Repair Log",
                "",
                "Status: `CHOICE_FULL_OPTION_BLOCKS_INSERTED_REVIEW_ONLY`",
                "",
                f"- target rows: {len(OPTION_BLOCKS)}",
                f"- patched rows: {len(patched)}",
                "",
                "## Patched Question IDs",
                "",
                *[f"- `{qid}`" for qid in patched],
                "",
                "## Note",
                "",
                "本补丁只补齐选择题完整选项显示，不改变原答案判断和陷阱解释。",
            ]
        )
        + "\n",
        encoding="utf-8",
    )
    print(f"patched={len(patched)}")


if __name__ == "__main__":
    main()
