def add_field(doc, label, text):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(4)
    p.paragraph_format.line_spacing = 1.15
    r = p.add_run(f"【{label}】")
    r.bold = True
    r.font.color.rgb = RGBColor(33, 87, 76)
    p.add_run(" " + clean_text(text).rstrip("。") + "。")
    return p


