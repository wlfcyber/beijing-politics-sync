def choice_patch(fields, node, suite_key, q, v4_choice_index, v6_choice_index):
    material = fields.get("材料触发点", "")
    question = fields.get("设问", "")
    landing = fields.get("答案落点", "")
    why = fields.get("为什么能想到", "")
    if (suite_key, q) in v4_choice_index:
        row = v4_choice_index[(suite_key, q)][0]
        question = clean_text(strip_line_ids(row.get("question_excerpt", "")))
        if not has_full_choice_question(question):
            return None
        material = clean_choice_signal(row.get("correct_trigger", "")) or material
        opt = row.get("correct_option", "")
        wrong = clean_wrong_notes(row.get("wrong_option_notes", ""))
        if re.search(r"选必|模块边界|必修四", material + wrong):
            return None
        why = make_why(material, node, "")
        if opt and wrong:
            landing = f"正确项是{opt}，因为{clean_text(material).rstrip('。')}；其他选项的毛病在于{wrong}。"
        elif opt:
            landing = f"正确项是{opt}，因为{clean_text(material).rstrip('。')}。"
        else:
            landing = make_landing(material, question, node, landing)
        return {"材料触发点": material, "设问": question, "为什么能想到": why, "答案落点": clean_text(landing)}
    if (suite_key, q) in v6_choice_index:
        row = v6_choice_index[(suite_key, q)][0]
        question = clean_text(row.get("stem_or_material", ""))
        if not has_full_choice_question(question):
            return None
        material = clean_choice_signal(row.get("material_trigger", "") or row.get("correct_chain", "") or material)
        opt = row.get("answer", "")
        wrong = clean_wrong_notes(row.get("wrong_or_boundary", ""))
        if re.search(r"选必|模块边界|必修四", material + wrong):
            return None
        why = make_why(material, node, "")
        if opt and wrong:
            landing = f"正确项是{opt}，因为{clean_text(material).rstrip('。')}；其他选项的毛病在于{wrong}。"
        elif opt:
            landing = f"正确项是{opt}，因为{clean_text(material).rstrip('。')}。"
        else:
            landing = make_landing(material, question, node, landing)
        return {"材料触发点": material, "设问": question, "为什么能想到": why, "答案落点": clean_text(landing)}
    if is_generic_question(question):
        return None
    if not has_full_choice_question(question):
        return None
    if re.search(r"选必|模块边界|必修四", material + landing + why):
        return None
    return {
        "材料触发点": clean_text(material),
        "设问": clean_text(question),
        "为什么能想到": make_why(material, node, why),
        "答案落点": make_landing(material, question, node, landing),
    }


