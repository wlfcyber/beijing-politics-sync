def normalize_suite(s: str) -> str:
    return re.sub(r"\s|高三|思政", "", str(s or ""))


def normalize_q(q: str) -> str:
    q = str(q or "")
    q = q.replace("（", "(").replace("）", ")")
    q = q.replace("第", "").replace("题", "").replace("问", "")
    return q.strip()


def parse_heading(heading: str):
    raw = re.sub(r"^\d+\.\s*", "", heading or "").strip()
    raw = raw.replace("高三", "").replace("思政", "")
    m = re.match(r"(?P<suite>\d{4}[^第]+?)\s*第(?P<q>.+?)题(?P<tail>.*)", raw)
    if not m:
        return {"suite": raw, "q": "", "qtype": "主观题" if "主观" in raw else "选择题" if "选择" in raw else ""}
    qtype = "选择题" if "选择题" in raw else "主观题" if "主观题" in raw else ""
    q = normalize_q(m.group("q"))
    tail = m.group("tail") or ""
    sub = re.search(r"第[（(]([一二三四五六七八九十\d]+)[）)]问", tail)
    if sub:
        q = f"{q}({sub.group(1)})"
    return {"suite": m.group("suite").strip(), "q": normalize_q(q), "qtype": qtype}


def title_from_parts(suite: str, q: str, qtype: str) -> str:
    q = normalize_q(q)
    sub = re.match(r"^(\d+)\(([^)]+)\)$", q)
    if sub:
        return f"{suite} 第{sub.group(1)}题第（{sub.group(2)}）问（{qtype}）" if qtype else f"{suite} 第{sub.group(1)}题第（{sub.group(2)}）问"
    return f"{suite} 第{q}题（{qtype}）" if qtype else f"{suite} 第{q}题"


