    for e in entries:
        by_node[(e["module"], e["node"])].append(e)

    inserted_images = set()
    first_module = True
    for module, nodes in FRAMEWORK.items():
        module_has = any(by_node.get((module, n)) for n in nodes)
        if not module_has:
            continue
        p = doc.add_heading(module, level=1)
        if first_module:
            p.paragraph_format.page_break_before = True
            first_module = False
        for node in nodes:
            rows = by_node.get((module, node), [])
            if not rows:
                continue
            doc.add_heading(node, level=2)
            for idx, e in enumerate(rows, 1):
                doc.add_heading(f"{idx}. {e['heading_base']}", level=3)
                if e.get("image") and e["image"] not in inserted_images and Path(e["image"]).exists():
                    p = doc.add_paragraph()
                    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
                    p.add_run().add_picture(e["image"], width=Inches(3.6))
