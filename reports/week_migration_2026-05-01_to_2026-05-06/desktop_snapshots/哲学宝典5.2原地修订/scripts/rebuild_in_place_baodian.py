def dedupe(entries):
    groups = defaultdict(list)
    for e in entries:
        key = (e["module"], e["node"], normalize_suite(e["suite"]), normalize_q(e["q"]), e["qtype"])
        groups[key].append(e)
    kept = []
    removed = []
    for key, rows in groups.items():
        rows.sort(key=entry_quality, reverse=True)
        kept.append(rows[0])
        removed.extend(rows[1:])
    return kept, removed


def sort_key(e):
    district = 99
    district_name = "zz"
    for d, idx in DISTRICT_ORDER.items():
        if d in e["suite"]:
            district = idx
            district_name = d
            break
    stage = 99
    for s, idx in STAGE_ORDER.items():
        if s in e["suite"]:
            stage = idx
            break
    year_match = re.search(r"20\d{2}", e["suite"])
    year = int(year_match.group(0)) if year_match else 0
    qtype = 0 if e["qtype"] == "主观题" else 1
    qnum = re.sub(r"\D", "", e["q"]) or "999"
    return (
        MODULE_INDEX.get(e["module"], 99),
        NODE_INDEX.get((e["module"], e["node"]), 99),
        qtype,
        district,
        district_name,
        -year,
        stage,
        int(qnum[:4]),
        e["suite"],
    )


