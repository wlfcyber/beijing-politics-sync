def strip_line_ids(text: str) -> str:
    text = str(text or "")
    text = re.sub(r"L\d+(?:/L?\d+)*(?:-L?\d+)?\s*(?:设问)?[:：]?", "", text)
    text = re.sub(r"L\d+(?:-L?\d+)?:\s*", "", text)
    text = re.sub(r"\s+", " ", text).strip()
    return text


def clean_text(text: str) -> str:
    text = strip_line_ids(text)
    text = re.sub(r"`[^`]+`", "", text)
    text = re.sub(r"/Users/[^，。；\s]+", "", text)
    text = re.sub(r"C:\\[^，。；\s]+", "", text)
    text = text.replace("从必修四角度看，", "")
    text = text.replace("从必修四角度看", "")
    text = text.replace("材料触发点：", "")
    text = text.replace("所以触发”", "因此能想到”")
    text = text.replace("所以触发", "因此能想到")
    text = re.sub(r"属选必[一二三][^。；]*[。；]?", "", text)
    text = re.sub(r"按必修四边界[^。；]*[。；]?", "", text)
    text = re.sub(r"触发必修四[^：:，,；。]*[:：]?", "", text)
    text = re.sub(r"必修四(?:唯物论|辩证法|认识论|历史唯物主义|矛盾观|联系观)?", "", text)
    text = text.replace("概念越界", "概念误用")
    text = text.replace("答案可以落在：", "")
    text = text.replace("答案可以落在", "")
    text = text.replace("可以写", "")
    text = text.replace("可写", "")
    text = re.sub(r"[^。！？；]*(?:可从|可以从|能够从)[^。！？；]{0,120}(?:角度|观点)[^。！？；]*[。！？；]?", "", text)
    text = re.sub(r"[^。！？；]*(?:阅卷细则|评分细则|细则|评分|参考答案|评标)[^。！？；]*[。！？；]?", "", text)
