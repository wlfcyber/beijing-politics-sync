def clean_choice_signal(text: str) -> str:
    text = clean_text(text)
    text = text.replace("正确项链：", "").replace("正确项链", "")
    text = text.replace("错肢：", "易错边界：").replace("错肢", "易错边界")
    text = re.sub(r"^[A-D]\s*=\s*[①②③④]+[。；，, ]*", "", text)
    text = re.sub(r"[①②③④]对应必修四[^：:]*[:：]", "", text)
