def build_summary(rows: list[tuple[str, str, str, str, str, str]]) -> list[list[str]]:
    """Aggregate per-node from per-mount rows."""
    grouped: dict[tuple[str, str], list[tuple[str, str, str, str]]] = defaultdict(list)
    for fn, pp, qid, mt, ev, note in rows:
        grouped[(fn, pp)].append((qid, mt, ev, note))

    summary: list[list[str]] = []
    for (fn, pp), entries in grouped.items():
        正文 = [q for q, m, e, n in entries if "正文" in m and "陷阱" not in m]
        陷阱 = [q for q, m, e, n in entries if "陷阱" in m]
        辅助挂载 = [q for q, m, e, n in entries if "辅助挂载" in m]
        辅助理解 = [q for q, m, e, n in entries if "辅助理解" in m]
        同类 = [q for q, m, e, n in entries if "同类索引" in m]
        blk = [q for q, m, e, n in entries if m.startswith("blocked")]
        exc = [q for q, m, e, n in entries if "excluded" in m]
        ev_set = ";".join(sorted({e for q, m, e, n in entries}))
        notes = " / ".join(n for q, m, e, n in entries if n)
        summary.append([
            fn, pp,
            ";".join(正文),
            ";".join(陷阱),
            ";".join(辅助挂载),
            ";".join(辅助理解),
            ";".join(同类),
            ";".join(blk),
            ";".join(exc),
            ev_set,
            notes,
        ])
    return summary


