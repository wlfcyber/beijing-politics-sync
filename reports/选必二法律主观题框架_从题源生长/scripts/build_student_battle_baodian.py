def read_rows() -> list[dict[str, str]]:
    with RUNS.open(encoding="utf-8-sig", newline="") as f:
        return list(csv.DictReader(f))


def shorten(text: str, n: int = 72) -> str:
    text = " ".join((text or "").replace("\n", " ").split())
    return text if len(text) <= n else text[: n - 1] + "…"


def clean_student_text(text: str) -> str:
    text = " ".join((text or "").replace("\n", " ").split())
    if "设问待" in text and "支持原告" in text:
        return "判断诉讼请求能否得到法院支持，并说明理由。"
    if "设问待" in text and "必修3" in text:
        return "结合法治与法律制度分析材料中的治理机制和作用。"
    if "设问待" in text:
        return "结合材料，运用法律知识分析。"
    replacements = {
        "设问待 OCR 回补；评分细则显示要求判断是否支持原告诉讼请求，并说明隐私权、消费…": "判断诉讼请求能否得到法院支持，并说明理由。",
        "设问待从试卷 OCR 回补；评标答案显示要求从必修3《政治与法治》和选必2《法律与…": "结合材料，从法治和法律制度角度分析。",
        "（2分）": "",
        "（3分）": "",
        "（4分）": "",
        "（5分）": "",
        "（6分）": "",
        "（7分）": "",
        "评分细则": "标准答案",
        "评标答案": "答案要求",
        "OCR": "原题",
    }
    for old, new in replacements.items():
        text = text.replace(old, new)
    return text


def trunk_name(row: dict[str, str]) -> str:
    node = row.get("framework_entry_node", "")
    label = row.get("baodian_label", "")
    qid = row.get("question_id", "")
    if label == "boundary_non_core":
        return "边界刹车"
    if label == "reference_only_demo":
        return "参考示范"
    if "OPEN" in node or "open" in node or "formal_open" in label or "open_container" in label:
        return "开放容器"
    if "001" in node:
        return "表格格子"
    if "005" in node:
        return "评判裁判"
    if "003" in node and "002" not in node:
        return "评判裁判"
    if "004" in node or "006" in node or qid in {"CC0143_2025_朝阳_一模_19"}:
        return "私法责任四链"
    if "008" in node:
        return "知识产权竞争"
    if "N06A" in node:
        return "风险合规"
    if "N06B" in node or "N06C" in node or "N06D" in node:
        return "程序救济"
    if "002" in node:
        return "意义价值"
    return "其他/待归位"


def row_by_id(rows: list[dict[str, str]], qid: str) -> dict[str, str]:
    return next(r for r in rows if r["question_id"] == qid)


def source_line(row: dict[str, str]) -> str:
    return f"{row['year']}年{row['district']}{row['exam_stage']}第{row['question_no']}题"


def status_name(row: dict[str, str]) -> str:
    label = row.get("baodian_label", "")
    return {
        "core_full_score_supported": "核心满分示范",
        "formal_open_container_partial": "正式题源开放容器",
        "reference_only_demo": "参考示范",
        "boundary_non_core": "边界刹车",
        "open_container_only": "开放容器",
    }.get(label, label)


def examples_for(rows: list[dict[str, str]], trunk: str, max_items: int = 8) -> str:
    items = [r for r in rows if trunk_name(r) == trunk and r.get("baodian_label") == "core_full_score_supported"]
    if not items:
        items = [r for r in rows if trunk_name(r) == trunk]
    parts = [f"{source_line(r)}（{shorten(clean_student_text(r['ask_text']), 28)}）" for r in items[:max_items]]
    return "；".join(parts) if parts else "暂无核心题源。"


def bullet_lines(items: list[str]) -> str:
    return "\n".join(f"- {item}" for item in items)


def student_start(row: dict[str, str]) -> str:
    trunk = trunk_name(row)
    label = row.get("baodian_label", "")
    if trunk == "表格格子":
        return "先看格名：机制、理由、意义、主体、责任、举证分别答，不能跨格套话。"
    if trunk == "评判裁判":
        return "第一句先判支持/不支持、有效/无效、构成/不构成，再写法律依据和材料事实。"
    if trunk == "私法责任四链":
        return "先定性：合同、侵权、消费、劳动、相邻或混合责任；再按规则、材料、责任写。"
    if trunk == "知识产权竞争":
        return "先判具体权利或竞争行为，再写法院手段和创新/竞争秩序价值。"
    if trunk == "程序救济":
        return "先判程序阶段和主体：维权准备、起诉、调解、公益诉讼或司法确认。"
    if trunk == "风险合规":
        return "一项风险对应一条法律边界，再对应一项可操作措施。"
    if trunk == "意义价值":
        return "先判评价对象和受益主体，再写权益保护、秩序规范、具体价值三层。"
    if trunk == "边界刹车" or label == "boundary_non_core":
        return "先刹车：按制度、机制、程序、效果写，不套合同侵权等私法模板。"
    if trunk == "参考示范" or label == "reference_only_demo":
        return "可参考答法，但不要把它背成核心模板；仍按设问和材料定入口。"
    return "按开放容器三句法：法条或制度、材料事实、具体功能；能写但不乱套。"


