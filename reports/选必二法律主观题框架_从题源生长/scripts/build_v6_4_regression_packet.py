def write_questions() -> None:
    lines = [
        "# V6.4 C/E/G/H 回归裸题包",
        "",
        "说明：本题包不提供题号、年份、地区、证据等级、core/guard/category 标签，也不提供答案和细则。请只依据 V6.4 学生稿训练出的判题方法作答。",
        "",
        f"可阅读的学生稿：`{HANDBOOK}`",
        "",
    ]
    for q in QUESTIONS:
        lines.extend(
            [
                f"## {q['sample']}",
                "",
                "### 材料",
                "",
                q["material"],
                "",
                "### 设问",
                "",
                q["ask"],
                "",
            ]
        )
    QUESTIONS_MD.write_text("\n".join(lines), encoding="utf-8")


def write_key() -> None:
    with KEY_CSV.open("w", encoding="utf-8-sig", newline="") as f:
        w = csv.DictWriter(
            f,
            fieldnames=[
                "sample",
                "question_id",
                "must_have_keywords",
                "grading_points",
                "failure_flags",
            ],
        )
        w.writeheader()
        for q in QUESTIONS:
            w.writerow({k: q[k] for k in w.fieldnames})


