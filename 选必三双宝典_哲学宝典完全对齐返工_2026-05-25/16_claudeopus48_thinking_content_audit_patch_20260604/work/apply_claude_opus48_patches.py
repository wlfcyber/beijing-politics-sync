def tagged(line: str, tag: str) -> bool:
    return line.startswith(f"【{tag}】")


def set_line(entry: dict, tag: str, text: str) -> None:
    prefix = f"【{tag}】"
    for i, line in enumerate(entry["lines"]):
        if line.startswith(prefix):
            entry["lines"][i] = f"{prefix} {text}"
            return
    entry["lines"].append(f"{prefix} {text}")


def append_line(entry: dict, tag: str, text: str) -> None:
    entry["lines"].append(f"【{tag}】 {text}")


def remove_tag(entry: dict, tag: str) -> None:
    entry["lines"] = [line for line in entry["lines"] if not tagged(line, tag)]


def line_text(entry: dict, tag: str) -> str:
    prefix = f"【{tag}】"
    for line in entry["lines"]:
        if line.startswith(prefix):
            return line[len(prefix):].strip()
    return ""


def new_entry(h1: str, h2: str, title: str, lines: list[str], source: str) -> dict:
    return {"h1": h1, "h2": h2, "title": title, "lines": lines, "added_by": source}


