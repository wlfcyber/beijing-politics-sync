def make_answer(entry: Entry) -> str:
    rubric = normalize_space(entry.fields.get("细则要点", ""))
    thinking = make_thinking(entry)
    targeted = TARGETED_ANSWER_FIXES.get((entry.title, entry.subtype))
    if targeted:
        return tidy_answer_punctuation(targeted)
    override = ANSWER_OVERRIDES.get((entry.title, entry.subtype))
    if override:
        return reduce_overlap(normalize_space(override), [rubric, thinking])
    old_answer = normalize_space(entry.fields.get("考生优秀答案", ""))
    if "选择题" in entry.title:
        answer = make_choice_answer(entry, old_answer or rubric)
    else:
        answer = make_answer_from_rubric(entry, old_answer or rubric)
    answer = reduce_overlap(answer, [rubric, thinking])
    answer = ANSWER_TEMPLATE_RE.sub("", answer)
    return tidy_answer_punctuation(answer)


def split_rubric_points(rubric: str) -> list[str]:
    text = normalize_space(rubric)
    if not text:
        return []
    text = re.sub(r"[；;]\s*", "。", text)
    sentences = [s.strip("。；; ") for s in split_sentences(text)]
    points = []
    for s in sentences:
        if not s:
            continue
        s = re.sub(r"^(因此|所以|故)", "", s)
        s = s.lstrip("，,。；;、 ")
        if len(s) > 120:
            pieces = re.split(r"，(?=(?:因此|所以|现在|材料|前提|结论|该|主张|答案|选项|推理))", s)
            points.extend([p.strip("，,。；;、 ") for p in pieces if p.strip("，,。；;、 ")])
        else:
            points.append(s)
    deduped = []
    for point in points:
        point = normalize_space(point)
        if point and point not in deduped:
            deduped.append(point)
    return deduped or [text]


