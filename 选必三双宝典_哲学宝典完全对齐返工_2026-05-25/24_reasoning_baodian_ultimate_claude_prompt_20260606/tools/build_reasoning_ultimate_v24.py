def add_text_block(doc: Document, text: str):
    paragraphs = []
    for line in material_lines(text):
        p = doc.add_paragraph(style="Question Material")
        p.paragraph_format.space_after = Pt(2)
        if re.match(r"^[ABCD][.．、]", line):
            p.paragraph_format.left_indent = Inches(0.22)
            p.paragraph_format.first_line_indent = Inches(-0.12)
        elif re.match(r"^[①②③④⑤⑥]", line):
            p.paragraph_format.left_indent = Inches(0.22)
        marker = re.match(r"^(材料[一二三四五六]?|【[^】]+】|AI 任务指令：|AI 结论：|据此，某同学作出如下推理：)(.*)$", line)
        if marker:
            r1 = p.add_run(marker.group(1))
            set_run_font(r1, size=10, bold=True, color="333333")
            if marker.group(2):
                r2 = p.add_run(marker.group(2))
                set_run_font(r2, size=10)
        else:
            r = p.add_run(line)
            set_run_font(r, size=10)
        paragraphs.append(p)
    return paragraphs[-1] if paragraphs else doc.add_paragraph(style="Question Material")


def add_toc_field(doc: Document):
    p = doc.add_paragraph()
