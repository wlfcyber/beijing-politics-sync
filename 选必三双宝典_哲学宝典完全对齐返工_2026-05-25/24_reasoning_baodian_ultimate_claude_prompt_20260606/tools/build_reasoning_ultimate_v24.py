def short_signal(entry: Entry) -> str:
    trigger = normalize_space(entry.fields.get("材料触发点", ""))
    sentences = split_sentences(trigger)
    if not sentences:
        return "先锁定题干给出的逻辑结构，再按教材规则判断。"
    s = sentences[0]
    s = re.sub(r"^材料", "", s)
    s = re.sub(r"^题干", "题干", s)
    if len(s) > 95:
        s = s[:95].rstrip("，；、 ") + "……"
    return s.rstrip("，；;、 ")


def make_exam_focus(entry: Entry) -> str:
    subtype = entry.subtype.replace("：", "，")
    signal = short_signal(entry)
    if "陷阱" in entry.subtype or "错误" in entry.subtype or "边界" in entry.subtype:
        focus = f"考{term_for(entry)}的边界。抓手是：{signal}"
    else:
        focus = f"考{term_for(entry)}。抓手是：{signal}"
    if focus and focus[-1] not in "。！？":
        focus += "。"
    return focus


def make_thinking(entry: Entry) -> str:
    why = normalize_space(entry.fields.get("为什么能想到", ""))
    sentences = split_sentences(why)
    cleaned = []
    for s in sentences:
        s = s.replace("这道题", "本题")
        s = s.replace("题目追问", "设问追问")
        s = re.sub(r"学生看到[“\"].*?[”\"].*?就应想到", "看到材料信号，就对应", s)
        s = re.sub(r"看到[“\"].*?[”\"].*?就应想到", "看到这个材料信号，就对应", s)
        if "不要泛写" in s and len(cleaned) >= 1:
            continue
        if s not in cleaned:
            cleaned.append(s)
        if len("".join(cleaned)) >= 210 or len(cleaned) >= 2:
            break
    if not cleaned:
        cleaned = [short_signal(entry)]
    result = "".join(cleaned)
    if len(result) > 260:
        result = result[:260].rstrip("，；、 ") + "。"
    result = result.rstrip("，；;、 ")
    if result and result[-1] not in "。！？":
        result += "。"
    return result


def first_conclusion_sentence(rubric: str) -> str:
    for s in split_sentences(rubric):
        if any(
            token in s
            for token in [
                "属于",
                "应判",
                "不符合",
                "符合",
                "不能",
                "可以",
                "答案",
                "正确",
                "错误",
                "成立",
                "无效",
                "有效",
            ]
        ):
            return s
    parts = split_sentences(rubric)
    return parts[0] if parts else ""


def last_conclusion_sentence(rubric: str) -> str:
    parts = split_sentences(rubric)
    if not parts:
        return ""
    for s in reversed(parts):
        if any(token in s for token in ["因此", "所以", "推出", "不能", "错误", "成立", "有效", "无效", "答案"]):
            return s
    return parts[-1]


