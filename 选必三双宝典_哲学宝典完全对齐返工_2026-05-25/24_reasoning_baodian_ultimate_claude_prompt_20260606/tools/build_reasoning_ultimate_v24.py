        combined = " ".join([material, answer, thinking, focus, rubric_points])

        expected_terms = MATERIAL_INTEGRITY_EXPECTATIONS.get(entry.title, [])
        missing_terms = [term for term in expected_terms if term not in material]
        if missing_terms:
            issues.append(
                {
                    "anchor": entry.title,
                    "type": "material_key_terms_missing",
                    "missing": "；".join(missing_terms),
                }
            )

        if entry.title in MISSING_SOURCE_TITLES:
            if material != "本题原始材料缺失，待补。":
                issues.append({"anchor": entry.title, "type": "missing_source_material_not_neutral"})
