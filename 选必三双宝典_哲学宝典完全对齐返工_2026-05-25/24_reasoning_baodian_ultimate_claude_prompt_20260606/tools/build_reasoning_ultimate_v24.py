@dataclass
class Entry:
    index: int
    major: str
    subtype: str
    raw_title: str
    title: str
    method_lines: list[str]
    fields: dict[str, str] = field(default_factory=dict)
    defect_flags: list[str] = field(default_factory=list)
    new: dict[str, object] = field(default_factory=dict)


def strip_enum(title: str) -> str:
    return re.sub(r"^\s*\d+\.\s*", "", title).strip()


def split_sentences(text: str) -> list[str]:
    text = normalize_space(text)
    if not text:
        return []
    parts = re.split(r"(?<=[。！？；])\s*", text)
    return [p.strip() for p in parts if p.strip()]


def normalize_space(text: str) -> str:
    text = text.replace("\u3000", " ")
    text = re.sub(r"[ \t]+", " ", text)
    text = re.sub(r"\n{3,}", "\n\n", text)
    return text.strip()


def clean_question_material(text: str) -> str:
    lines = []
    for raw in text.splitlines():
        line = raw.strip()
        if line in {"```text", "```"}:
            continue
        if re.fullmatch(r"高三政治", line):
            continue
        if "高三年级" in line and "思想政治" in line:
            continue
        if "扫描全能王" in line or "3亿人都在用" in line:
            continue
        if "考生务必将答案答在答题卡" in line:
            continue
        if re.search(r"思想政治\s*第\s*页", line):
            continue
        if re.fullmatch(r"\.{6,}", line):
            continue
        if re.fullmatch(r"[.·…]{5,}", line):
            continue
        for pat in FOOTER_PATTERNS:
            line = pat.sub("", line)
        line = line.replace("成沟", "成为")
        line = line.replace("消赞", "消费")
        line = line.replace("环搅", "环境")
        line = line.replace("高梁", "高粱")
        line = line.replace("划线1处", "划线①处").replace("划线2处", "划线②处")
        line = re.sub(r"\(\s*(\d+)\s*分\s*\)", r"（\1分）", line)
        line = re.sub(r"（(\d+)\s+分）", r"（\1分）", line)
        line = re.sub(r"\.{6,}", "……", line)
        line = normalize_space(line)
        if re.fullmatch(r"高三政治", line):
            continue
        if line == ",":
            line = "划线①：__________"
        elif line == "2":
            line = "划线②：__________"
        if re.fullmatch(r"[.。．]", line):
            continue
        if line:
            lines.append(line)
    cleaned = "\n".join(lines).strip()
    cleaned = re.sub(r"\(\s*(\d+)\s*\n\s*分\s*\)", r"（\1分）", cleaned)
    cleaned = re.sub(r"2\.83\s*化元", "2.83亿元", cleaned)
