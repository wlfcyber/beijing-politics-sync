def compact_visible_text(text: str) -> str:
    return re.sub(r"[\s，,。；;：:、（）()\[\]【】《》“”\"'·•\-—_？?！!]+", "", normalize_space(text))


def strip_score_tail(text: str) -> str:
    return re.sub(r"\d+分$", "", compact_visible_text(text))


def material_contains_question(material: str, question: str) -> bool:
    material = normalize_space(material)
    question = normalize_space(question)
    if not material or not question:
        return False
    if question in material:
        return True
    mq = compact_visible_text(material)
    qq = strip_score_tail(question)
    if qq and len(qq) >= 12 and qq in mq:
        return True
    candidates = [strip_score_tail(part) for part in re.split(r"[\n。！？?；;]", material)]
    candidates = [c for c in candidates if len(c) >= 10]
    for candidate in candidates:
        if not qq:
            continue
        if len(candidate) >= 12 and candidate in qq and len(candidate) / max(len(qq), 1) >= 0.65:
            return True
        ratio = SequenceMatcher(None, qq, candidate).ratio()
        if ratio >= 0.78:
            return True
    return False


def should_show_question(entry: Entry) -> bool:
    material = str(entry.new.get("题目材料", ""))
    question = str(entry.new.get("设问", ""))
    if not normalize_space(question):
        return False
    if material_contains_question(material, question):
        return False
    return True


