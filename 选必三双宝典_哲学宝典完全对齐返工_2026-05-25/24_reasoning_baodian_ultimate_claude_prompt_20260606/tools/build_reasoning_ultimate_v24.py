def choice_letter(text: str) -> str:
    match = re.search(r"(?:答案选|应选)\s*([A-D])", text)
    if match:
        return match.group(1)
    match = re.search(r"\b([A-D])\s*项正确", text)
    if match:
        return match.group(1)
    return ""


