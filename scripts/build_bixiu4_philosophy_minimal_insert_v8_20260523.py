def insert_blocks_into_md(base: str) -> str:
    out = base
    for anchor, blocks in INSERTS.items():
        if anchor not in out:
            raise RuntimeError(f"missing anchor: {anchor}")
        payload = "\n\n" + "\n\n".join(blocks)
        out = out.replace(anchor, anchor + payload, 1)
    return out


def verify_only_insertions(base: str, patched: str) -> bool:
    stripped = patched
    for blocks in INSERTS.values():
        for block in blocks:
            stripped = stripped.replace("\n\n" + block, "", 1)
    return stripped == base


def write_csv(path: Path, rows: list[dict[str, str]], fields: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fields, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)


def insert_paragraph_after(paragraph: Paragraph, text: str = "", style=None) -> Paragraph:
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_para = Paragraph(new_p, paragraph._parent)
    if style is not None:
        new_para.style = style
    if text:
        new_para.add_run(text)
    return new_para


def add_label_paragraph_after(paragraph: Paragraph, line: str) -> Paragraph:
    m = re.match(r"^\*\*(.+?)\*\*：(.+)$", line)
    new_para = insert_paragraph_after(paragraph, "")
    if m:
        r1 = new_para.add_run(m.group(1) + "：")
        r1.bold = True
        new_para.add_run(m.group(2))
    else:
        new_para.add_run(line)
    return new_para


def insert_blocks_into_docx(base_docx: Path, out_docx: Path) -> None:
    doc = Document(base_docx)
    used: set[str] = set()
    for anchor, blocks in INSERTS.items():
        anchor_text = anchor[3:].strip()
        para = next((p for p in doc.paragraphs if p.text.strip() == anchor_text), None)
        if para is None:
            raise RuntimeError(f"missing docx anchor: {anchor_text}")
        cur = para
        for block in blocks:
            for line in block.splitlines():
                if not line.strip():
                    cur = insert_paragraph_after(cur, "")
                elif line.startswith("## "):
                    cur = insert_paragraph_after(cur, line[3:].strip(), style=para.style)
                elif line.startswith("**"):
                    cur = add_label_paragraph_after(cur, line)
                else:
                    cur = insert_paragraph_after(cur, line)
        used.add(anchor)
    out_docx.parent.mkdir(parents=True, exist_ok=True)
    doc.save(out_docx)


def build_audit() -> str:
    source_counter: defaultdict[str, int] = defaultdict(int)
    for blocks in INSERTS.values():
        for block in blocks:
            m = re.search(r"\*\*来源题目\*\*：(.+)", block)
            if m:
                source_counter[m.group(1).strip()] += 1
    lines = [
        "# 认可版最小插入审计",
        "",
        f"生成时间：{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
        "",
        "## 底稿",
        "",
        f"- Markdown：`{BASE_MD}`",
        f"- Word：`{BASE_DOCX}`",
        "",
        "## 本次原则",
        "",
        "- 不重排原宝典。",
        "- 不改写原宝典已有正文。",
        "- 只把新增题插入到已有知识点标题后。",
        "- 跨书边界和证据不足题只进审计，不进学生正文。",
        "",
        "## 插入统计",
        "",
        "| 来源题目 | 插入次数 |",
        "|---|---:|",
    ]
    for source, count in sorted(source_counter.items()):
        lines.append(f"| {source} | {count} |")
    lines.extend(
        [
            "",
            "## 未插入正文的边界项",
            "",
            "| 套卷 | 题号 | 决定 | 理由 |",
            "|---|---|---|---|",
        ]
    )
    for row in BOUNDARY_ROWS:
        lines.append(f"| {row['suite']} | {row['question']} | {row['decision']} | {row['reason']} |")
    return "\n".join(lines) + "\n"


